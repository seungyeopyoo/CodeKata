const solution = function (a, b) {
	const week = ['SUN', 'MON', 'TUE', 'WED', 'THU', 'FRI', 'SAT'];

	const date = new Date(2016, a - 1, b); // a-1 하는 이유는 자바스크립트는 0월달 부터 시작한다고함 ;
	return week[date.getDay()]; // getDay() 메서드는 주어진 날짜의 현지 시간 기준 요일을 반환 Sunday ~ Saturday : 0 ~ 6
};

